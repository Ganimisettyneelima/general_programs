class  numberpattern5
{
	public static void main(String[] args) 
	{
		int num1=0;
	     int num2=-1;
		 for(int i=1;i<=5;i++)
		{
			 num1++;
			 num2++;
			 for(int j=1;j<=num1;j++)
			{
				 System.out.print(j);

		 }
		 for(int k=num2;k>=1;k--)
			{
               System.out.print(k);
			}


		System.out.println();
	}
}
}
